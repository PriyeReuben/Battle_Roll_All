class Creature:
    def __init__(self, name, proficiency_bonus, strength_mod, dexterity_mod, casting_mod, armor_class):
        self.name = name
        self.proficiency_bonus = proficiency_bonus
        self.strength_mod = strength_mod
        self.dexterity_mod = dexterity_mod
        self.casting_mod = casting_mod
        self.armor_class = armor_class
